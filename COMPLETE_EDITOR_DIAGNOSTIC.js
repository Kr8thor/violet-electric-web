    // PHASE 5: CONTENT DETECTION
    // ===================================
    console.log('\nüéØ PHASE 5: CONTENT DETECTION DIAGNOSTIC');

    function testContentDetection() {
        return new Promise((resolve) => {
            console.log('üéØ Testing content detection in React app...');

            const iframe = document.getElementById('violet-site-iframe');
            if (!iframe || !iframe.contentWindow) {
                console.log('‚ùå Cannot test content detection - iframe not accessible');
                resolve(false);
                return;
            }

            // Send content detection request
            iframe.contentWindow.postMessage({
                type: 'violet-check-components',
                timestamp: Date.now()
            }, '*');

            const contentListener = (event) => {
                if (event.data?.type === 'violet-component-info') {
                    console.log('üìä React component info received:', event.data);
                    
                    if (event.data.editableElements > 0) {
                        console.log(`‚úÖ Found ${event.data.editableElements} editable elements`);
                        diagnosticResults.react.contentDetection = true;
                    } else {
                        console.log('‚ö†Ô∏è No editable elements found');
                        diagnosticResults.issues.push('No data-violet-field elements detected');
                    }
                    
                    window.removeEventListener('message', contentListener);
                    resolve(true);
                }
            };

            window.addEventListener('message', contentListener);

            // Timeout if no response
            setTimeout(() => {
                window.removeEventListener('message', contentListener);
                console.log('‚ö†Ô∏è No content detection response received');
                resolve(false);
            }, 2000);
        });
    }

